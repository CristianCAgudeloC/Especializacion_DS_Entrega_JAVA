package Model;

/**
 *
 * @author camil
 */
public class Product {
    private int id;
    private String name;
    private String description;
    private int price;
    private String type;

    public Product() {
    }

    public Product(int id, String name, String descriptio, int price, String type) {
        this.id = id;
        this.name = name;
        this.description = descriptio;
        this.price = price;
        this.type = type;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String descriptio) {
        this.description = descriptio;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
    
    
    
}
